uint16_t TSMS_NT35310_readId(TSMS_SCHP screen) {
	__internal_tsms_lcd_write_command(screen, 0XD4);
	uint16_t id;
	id = __internal_tsms_lcd_read_data(screen);
	id = __internal_tsms_lcd_read_data(screen);
	id = __internal_tsms_lcd_read_data(screen);
	id <<= 8;
	id |= __internal_tsms_lcd_read_data(screen);
	return id;
}

